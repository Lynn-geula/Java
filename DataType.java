public class DataType {
    public static void main(Demo a[]) 
    {
        int h = 3;
        byte i = 127;
        short j = 23;
        long k  = 12345;
        float l  = 4.5f;
        double m = 33.3;
        char n = 'L';
        System.out.println(h);
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
        System.out.println(l);
        System.out.println(m);
        System.out.println(n);
    }
}
