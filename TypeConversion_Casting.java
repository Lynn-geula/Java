public class TypeConversion_Casting {
    public static void main(Demo[] args) {
        byte b = 125;
        int a = b;
        System.out.println(a);
        
        int l = 12;
        byte k = (byte)l;
        System.out.println(k);
        
        float f = 6.7f;
        int g = (int)f;
        System.out.println(g);
    }
    
}
