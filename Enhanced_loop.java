public class Enhanced_loop {
    public static void main(Demo args[]) 
    {
         
        int nums[] = new int[4];
        nums[0] = 1;
        nums[1] = 4;
        nums[2] = 0;
        nums[3] = 3;


        for(int n : nums)
        {
            System.out.println(n);
        }

    }
    
}
